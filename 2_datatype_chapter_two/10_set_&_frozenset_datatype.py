# Now we will see more three set methods, which is very important
"""
1. subset :- set A is a subset of set B if EVERY element of A is also present in B
2. superset :- set A is a superset of B if A contains every element of B. it's just reverse of subset
3. disjoint :- Two sets are disjoint if they share NOTHING
"""
# and output of all this three set are in boolean, if it's correct then it will return True , if it's not correct it will return False
# it do it pratically

set_a = {5 , 6}
set_b = {3 , 4 , 5 , 6 , 7}
set_c = {3 , 4}
set_d = {9 , 0}
print(set_a.issubset(set_b))
print(set_a.issuperset(set_b))
# NOTE :- set_a.issubset(set_b) == set_b.superset(set_a)
print(set_c.isdisjoint(set_a))
print("\n")

# there is operator for subset and superset , but there is no operator for disjoint you should use the .isdisjoint() method 
print(set_d < set_b) # subset
print(set_b > set_a) # superset


