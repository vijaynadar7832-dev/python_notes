# which means once the set is created it cannot be modified
# which means we can only use read only operations like len() , .union() , .intersection() , .difference() , .symmetric_difference() , .isdisjoint , .issubset() , .issuperset()
print("\n")
# now we will see methods to create frozenset 
# frozenset can only be stored inside the iterables

# method 1 (using set iterable)
frozen_1 = frozenset({1 , 2 , 3 , (3, 5) , "hello"})
print(frozen_1 , type(frozen_1) , sep="\n")
