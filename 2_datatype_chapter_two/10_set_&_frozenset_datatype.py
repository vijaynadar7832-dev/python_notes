# Mathematical set operations
# to do mathematical set operations there are two ways first is using operators and second is using python built-in methods , so let's discover both the parts
"""
first we will understand basic mathematical set operations
1. Union (A U B):- Union returns a set containing every element that exists in either of the two sets (without duplicates)
2. Intersection (A ∩ B):- Intersection returns elements that are present in BOTH sets.
3. Difference (A - B):- Difference returns the elements that exist in the first set but NOT in the second set. Which means, Give me everything from A that is NOT in B.
4. Symmetric Difference (A U B) - (A ∩ B):- Symmetric difference removes the common element from both sets and returns the remaining elements.
"""


A = {1 , 2 , 3 , 4}
B = {3 , 4 , 5 , 6 , 7}

# before moving I want to explain you one thing we will use In-place version, which means instead of creating a new set it will modify the original set

# using operators
# Union
union = A | B 
print(union)

# Intersection
intersection = A & B 
print(intersection)

# Difference
difference_1 = A - B 
print(difference_1)
difference_2 = B - A 
print(difference_2)

# Symmetric difference
symmetric_difference = A ^ B 
print(symmetric_difference)
print("\n")
#------------------------------------------------------

# using methods
# union
union_method = A.union(B)
print(union_method)

# Intersection
intersection_method = A.intersection(B)
print(intersection_method)

# Difference
difference_method = A.difference(B)
print(difference_method)

# Symmetric difference
symmetric_difference_method = A.symmetric_difference(B)
print(symmetric_difference_method)
print("\n")
# NOTE = we cannot use the operators in other iterables like list and tuple , but we can use the methods in other iterables


