# set is an unordered, mutable built-in datatype used to store multiple unique elements. It does not allow duplicate values, and its elements must be immutable.
"""
now we can get doubt that if set is mutable then how can it's elements can be immutable 
A set is a mutable datatype, meaning elements can be added or removed after creation. 
However, the elements inside a set must be immutable because Python uses hashing 
to determine where each element is stored. Immutable objects provide a stable hash 
value, allowing sets to function efficiently and prevent data corruption.
So, that's why set should have immutable items
"""


