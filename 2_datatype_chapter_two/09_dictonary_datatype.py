# Modifying elements
author = {"name" : "Romeo" , 
          "age" : 25 , 
          "gender" : "male" , 
}

# modifying single values
author["age"] = 26 
print(author)

# modifiyig multiple values
author.update({"name" : "Maria" , "gender" : "female"})
