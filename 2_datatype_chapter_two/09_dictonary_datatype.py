# At second line I mentioned that dictionary is mutable but it's keys should be immutable datatype, but why?
# let's understand in detail how dictionary works internally.
"""
Python dictionaries use a data structure called a Hash Table.

When we create a dictionary:

data = {"name": "Vijay"}

Python does not search the key directly.
Instead, it performs two steps:

1. Python converts the key into a hash value using the hash() function.
   A hash is an integer that remains constant while the program is running.

   Example:
   hash("name") → 59384734

2. Python uses this hash to determine the memory location
   where the key-value pair should be stored.

--------------------------------------------------

Why must dictionary keys be immutable?

Because the hash of a key must NEVER change.

If a key were mutable and got modified:

old key → old hash → stored at one location  
modified key → new hash → Python searches another location  

As a result, the dictionary would not be able to find the value,
breaking fast lookup performance.

For this reason, Python only allows hashable (immutable) objects as keys,
such as:

✔ int
✔ str
✔ bool
✔ float
✔ tuple (if it contains only immutable elements)

Mutable objects like list, set, and dict are not allowed
because their values can change, which would change their hash.
"""

