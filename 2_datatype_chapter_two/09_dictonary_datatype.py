# Removing

# Remove specific pair
author.pop("birthdate")
print(author)
author.pop("system", "Not Found") # it prevents error if no "system" key is found
print(author)


# Remove last inserted pair
author.popitem()
print(author)

# Remove specific pair
del author["gender"]
print(author)

# NOTE = now you can think that `author.pop()` and `del author[ ]` are performs same work then why there are two ways to remove a pair
# `author.pop()` is used when you are not sure about the key ; because you can avoid error by giving extra arguments inside the pop() after the key name
# `del author[ ]` is used when you are sure about the key ; but if you enter any wrong key then it will throw error

# removes all the elements inside the dictionary
author.clear()
print(author)

# remove dictionary
del author
# print(author) ❌ it will give NameError: name 'author' is not defined
# it totally wipes the dictionary and also deletes the variable name from the memory
