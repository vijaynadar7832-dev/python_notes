# memoryview is a built-in binary datatype that let's you access the memory of another object directly without copying it

"""
+ Why does memoryview exist?
- Because copying large binary data is expensive in terms of memory and speed.

# Real problem
create = bytes("hello", "utf-8")
slice_create = create[0:3]
print(slice_create)

This looks harmless, but Python internally creates a NEW object
and copies the sliced data ("hel") into new memory.

For small data this is fine, but for very large data
(like a 500MB file), repeated copying can slow down the program
and waste RAM.

That's why memoryview exists.

memoryview does NOT copy data.
It creates a view that references the SAME memory buffer.
"""

"""
# and memoryview supports buffer protocol
# buffer protocol provides a way to access the internal data of an object, this internal data is a memory array or buffer
# the buffer protocol allows one object to expose it's internal data(buffer) and other to access the buffer without intermediate copying
"""

# NOTE = always remember that memory dosen't store data or copy it only point to the original data
# NOTE = memoryview is mutable datatype, but if we point it to bytes then we cannot modify, but if we point it to bytearray then we can modify
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

"""
memoryview supports only those objects that expose their data through the buffer protocol , which means they store data in raw binary memory
(i.e., allow safe direct access to their internal memory).

That is why memoryview works with objects like:
bytes, bytearray, array, and mmap.

It does NOT work with objects such as:
str, int, float, bool, list, tuple, range, complex, dict, set, and frozenset
because they do not expose a buffer.
"""


#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

# there is only one syntax of memoryview datatype, which is first store the data in which the memoryview support then convert it to memoryview

data_bytearray = bytearray([64 , 92 , 73 , 123])
for i in data_bytearray:
    print(repr(chr(i)))
print("\n")

mv = memoryview(data_bytearray)
print(mv , type(mv) , sep="\n")

data_bytearray[1] = 95 # we modified the bytearray
print("\n")

for i in data_bytearray:
    print(repr(chr(i)))
print("\n")

print(list(data_bytearray))
print(list(mv))
print("\n")
# you can see that we only done changes in data_bytearray but it is reflecting on mv also


#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
#---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

# now we will see methods and attributes which is used in memoryview datatype

# .readonly
print(mv.readonly)
# it prints output as True or False; where True -> cannot modify , False -> can modify

# .nbytes
print(mv.nbytes)
# it print the total usage of memory

# .itemsize
print(mv.itemsize)
# it print the size of one element in bytes

# .format
print(mv.format)
# it tells the type of data stored

# .tolist()
print(mv.tolist())
# converts the memory into list


